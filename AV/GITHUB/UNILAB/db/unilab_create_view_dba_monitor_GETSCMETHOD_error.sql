PART_NO            NOT NULL VARCHAR2(18 CHAR) 
REVISION           NOT NULL NUMBER(4)         
DATE_READY                  DATE              
DATE_PROCEED                DATE              
RESULT_PROCEED              NUMBER(1)         
DATE_TRANSFERRED            DATE              
RESULT_TRANSFER             NUMBER(1)         
