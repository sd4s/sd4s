select dwc.mainpart, dwc.component_status, count(*)
from DBA_V_WEIGHT_COMP_PART dwc
where dwc.component_status not in ('CRRNT QR3','CRRNT QR4','CRRNT QR5')
AND  ( dwc.mainpart like 'E%'
     OR dwc.mainpart like 'G%'
     )
group by dwc.mainpart, dwc.component_status
;


