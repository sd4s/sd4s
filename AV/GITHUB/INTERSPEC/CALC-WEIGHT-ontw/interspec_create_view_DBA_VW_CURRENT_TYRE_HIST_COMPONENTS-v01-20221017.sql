create or replace view DBA_V_CRRNT_TYRE_HIST_COMP
(mainpart
,mainpart_status
,component_status
,aantal_hist
)
AS
select d.mainpart
,      (select dwcM.header_status from DBA_V_WEIGHT_COMP_PART dwcM where dwcM.mainpart = d.mainpart  and   dwcM.mainpart = dwcM.part_no and   dwcM.component_part_no is null) mainpart_status
,      d.component_status
,      d.aantal_hist
from   (select dwc.mainpart, dwc.component_status, count(*) aantal_hist
		from DBA_V_WEIGHT_COMP_PART dwc
		where dwc.component_status not in ('CRRNT QR3','CRRNT QR4','CRRNT QR5')
		AND  ( dwc.mainpart like 'E%'
			OR dwc.mainpart like 'G%'
			)
		group by dwc.mainpart, dwc.component_status
		) d
ORDER by d.mainpart, d.component_status
;

