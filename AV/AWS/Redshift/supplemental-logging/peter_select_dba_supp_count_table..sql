select a.asc_id
,      a.asc_schema_owner
,      a.asc_table_name
,      a.asc_check_date
,      a.asc_count
,      a.asc_opmerking
from dba_aws_supplemental_count a
where asc_table_name in (select b.asc_table_name
                         from dba_aws_supplemental_count B
						 where b.asc_opmerking is not NULL
						 and   b.asc_check_date = (select max(c.asc_check_date) from dba_aws_supplemental_count c where c.asc_table_name = b.asc_table_name )
						)
order by a.asc_table_name
,        a.asc_check_date
;

