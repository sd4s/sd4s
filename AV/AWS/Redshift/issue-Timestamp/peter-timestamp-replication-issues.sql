--TIMESTAMPS
--TIMESTAMP WITH TIME ZONE is a variant of TIMESTAMP that includes a time zone offset or time zone region name in its value. 
--The time zone offset is the difference (in hours and minutes) between local time and UTC (Coordinated Universal Time, formerly Greenwich Mean Time). 
--Specify the TIMESTAMP WITH TIME ZONE datatype as follows:
--TIMESTAMP [(fractional_seconds_precision)] WITH TIME ZONE
--fractional_seconds_precision is optional and specifies the number of digits in the fractional part of the SECOND datetime field.
--
--TIMESTAMP WITH LOCAL TIME ZONE is another variant of TIMESTAMP. 
--It differs from TIMESTAMP WITH TIME ZONE as follows: 
--data stored in the database is normalized to the database time zone, and the time zone offset is not stored as part of the column data. 
--When users retrieve the data, Oracle returns it in the users' local session time zone. 
--The time zone offset is the difference (in hours and minutes) between local time and UTC (Coordinated Universal Time, formerly Greenwich Mean Time).
--Specify the TIMESTAMP WITH LOCAL TIME ZONE datatype as follows:
--TIMESTAMP [(fractional_seconds_precision)] WITH LOCAL TIME ZONE
--fractional_seconds_precision is optional and specifies the number of digits in the fractional part of the SECOND datetime field.
--There is no literal for TIMESTAMP WITH LOCAL TIME ZONE, but TIMESTAMP literals and TIMESTAMP WITH TIME ZONE literals can be inserted into a TIMESTAMP WITH LOCAL TIME ZONE column.
--The default date format for TIMESTAMP WITH LOCAL TIME ZONE is determined by the value of the NLS_TIMESTAMP_FORMAT initialization parameter.

